    public void rebuildSemesterComboBoxes() {
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        currentSemesterComboBox.setModel(new javax.swing.DefaultComboBoxModel(semesters.toArray()));
        if (!semesters.isEmpty()) {
            currentSemesterLabel.setText(semesters.get(0));
            currentSemester = semesters.get(0);
        } else {
            currentSemesterLabel.setText("None, add a semester.");
            currentSemester = "None";
        }
    }
    
    public void rebuildCourseComboBoxes() {
        ArrayList<String> courses = CourseQueries.getAllCourseCodes();
        classCourseCmbo.setModel(new javax.swing.DefaultComboBoxModel(courses.toArray()));
        rebuildStudentClassComboBox(previousStudentSelectedInDropClass);
    }
    
    public void rebuildClassComboBoxes() {
        ArrayList<String> classes = ClassQueries.getSemesterClassCodes(currentSemester);
        
        selectClassCmbo.setModel(new javax.swing.DefaultComboBoxModel(classes.toArray()));
        displayClassListCmbo.setModel(new javax.swing.DefaultComboBoxModel(classes.toArray()));
        dropClassAdminCmbo.setModel(new javax.swing.DefaultComboBoxModel(classes.toArray()));
        rebuildStudentClassComboBox(previousStudentSelectedInDropClass);
    }
    
    public void rebuildStudentComboBoxes() {
        ArrayList<StudentEntry> students = StudentQueries.getStudentList();
        ArrayList<String> names = new ArrayList<String>();
        for(StudentEntry s: students){
            names.add(s.getLastName()+", "+s.getFirstName());
        }
        scheduleSelectStudentCmbo.setModel(new javax.swing.DefaultComboBoxModel(names.toArray()));
        displaySelectStudentCmbo.setModel(new javax.swing.DefaultComboBoxModel(names.toArray()));
        dropStudentCmbo.setModel(new javax.swing.DefaultComboBoxModel(names.toArray()));
        dropClassStudentStudentCmbo.setModel(new javax.swing.DefaultComboBoxModel(names.toArray()));
      
    }
    
    public void rebuildStudentClassComboBox(StudentEntry student){
        ArrayList<ScheduleEntry> studentSchedule = ScheduleQueries.getScheduleByStudent(currentSemester, student.getStudentID());
        ArrayList<String> codes = new ArrayList<String>();
        for(ScheduleEntry entry : studentSchedule){
            codes.add(entry.getCourseCode());
        }
        dropClassStudentClassCmbo.setModel(new javax.swing.DefaultComboBoxModel(codes.toArray()));
    }

