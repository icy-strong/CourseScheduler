    private void dropStudentSubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentSubmitBtnActionPerformed
        // TODO add your handling code here:
        String s = "";
        String s2 = "\n";
        Object currStudent = dropStudentCmbo.getSelectedItem();
        String studentName = (String) currStudent;
        String[] name = studentName.split(", ");
        StudentEntry student = StudentQueries.getStudentByName(name[0], name[1]);   
        s+= studentName + ", " + student.getStudentID() + " has been dropped from the student list.\n\n";
        ArrayList<String> sems = SemesterQueries.getSemesterList();
        
        for (String sem : sems){
            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(sem, student.getStudentID());
            if (!schedule.isEmpty()){s+= sem + ":\n";}
            for (ScheduleEntry entry : schedule){
                ScheduleQueries.dropStudentScheduleByCourse(sem, student.getStudentID(),entry.getCourseCode());
                s+= student.getLastName() + ", " + student.getFirstName() + ", " +student.getStudentID()+
                         " has been dropped from " + entry.getCourseCode() + ".\n";
                ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByClass(sem, entry.getCourseCode());
                    if (!waitlisted.isEmpty()){
                        ScheduleEntry newClass = waitlisted.get(0);
                        ScheduleQueries.updateScheduleEntry(newClass);
                        StudentEntry newStudent = StudentQueries.getStudent(newClass.getStudentID());
                        s2+= newStudent.getLastName() + ", " + newStudent.getFirstName() + ", " +newStudent.getStudentID()+
                         " has been scheduled for " + newClass.getCourseCode() + ".\n";
                    }
            }
            s+=s2 + "\n";
            s2="";
        }
        
        StudentQueries.dropStudent(student.getStudentID());
        dropStudentTxtBox.setText(s);
        rebuildStudentComboBoxes();
        
        
    }//GEN-LAST:event_dropStudentSubmitBtnActionPerformed

