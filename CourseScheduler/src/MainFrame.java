        Object currStudent = scheduleSelectStudentCmbo.getSelectedItem();
        String studentName = (String) currStudent;
        String[] name = studentName.split(", ");
        StudentEntry student = StudentQueries.getStudentByName(name[0], name[1]);
        int usedSeats = ScheduleQueries.getScheduledStudentCount(currentSemester, curCls);
        int totSeats = ClassQueries.getClassSeats(currentSemester, curCls);
        
